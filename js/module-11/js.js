const root = document.querySelector("#root");
const form = document.querySelector(".js-form");

const btnFilter = form.querySelector('button[type="submit"]');
const btnClear = form.querySelector('button[type="reset"]');

const source = document.querySelector("#cards-tmpl").innerHTML.trim();
const template = Handlebars.compile(source);

btnFilter.addEventListener("click", handleFilterItems);
btnClear.addEventListener("click", handleClearForm);
const inputs = form.querySelectorAll("input");

function handleFilterItems(event) {
  event.preventDefault();
  const filter = { size: [], color: [], release_date: [] };
  root.innerHTML = "";

  inputs.forEach(input => {
    if (input.name === "size" && input.checked) {
      filter.size.push(input.value);
    }
    if (input.name === "color" && input.checked) {
      filter.color.push(input.value);
    }
    if (input.name === "release_date" && input.checked) {
      filter.release_date.push(input.value);
    }
  });

  sorterLaptop(laptops, filter)
  
    
}

function handleClearForm() {
  event.preventDefault();
  form.reset();
}

function sorterLaptop (arrOfGoods, arrOfFilter) {
  const apply = arrOfGoods.filter(item => {
    const getBySize =
    arrOfFilter.size.length !== 0
        ? arrOfFilter.size.includes(String(item.size))
        : true;
    const getByColor =
    arrOfFilter.color.length !== 0
        ? arrOfFilter.color.includes(String(item.color))
        : true;
    const getByReleaseDate =
    arrOfFilter.release_date.length !== 0
        ? arrOfFilter.release_date.includes(String(item.release_date))
        : true;
    return getBySize && getByColor && getByReleaseDate;
  });
  renderHTML(apply);
}

function renderHTML(arr) {
  const markup = arr.reduce((acc, laptop) => acc + template(laptop), "");
  root.insertAdjacentHTML("afterbegin", markup);
}